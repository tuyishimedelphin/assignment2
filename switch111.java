 class Switchone {  
public static void main(String[] args) {  
      
    int number=5;  
      
    switch(number){  
      
    case 5: System.out.println("5");  
    break;  
    case 10: System.out.println("10");  
    break;  
    case 20: System.out.println("20");  
    break;  
   
    default:System.out.println("Not in 5, 10 or 20");  
    }  
}  
}  